#ifndef __CBITMAP_H__
#define __CBITMAP_H__

#include <vector>
#include <string>

class cbitmap {
private:
	int byte_width;
	int width;
	int height;
	std::vector<unsigned char> image;

public:
	// --------------------------------------------------------------------
	// コンストラクタ
	// --------------------------------------------------------------------
	cbitmap(): byte_width( 0 ), width( 0 ), height( 0 ) {
	}

	// --------------------------------------------------------------------
	//	ロード
	// --------------------------------------------------------------------
	void load( std::string s_file_name );

	// --------------------------------------------------------------------
	//	画素値
	// --------------------------------------------------------------------
	void get_pixel( int x, int y, unsigned char &r, unsigned char &g, unsigned char &b ) const;

	// --------------------------------------------------------------------
	//	プロパティ取得
	// --------------------------------------------------------------------
	int get_width( void ) const{
		return this->width;
	}

	// --------------------------------------------------------------------
	int get_height( void ) const{
		return this->height;
	}
};

#endif
